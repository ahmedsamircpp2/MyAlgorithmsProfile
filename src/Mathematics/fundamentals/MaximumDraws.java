import java.util.Scanner;

public class MaximumDraws {
    public static void main(String[] args) {
        Scanner in = new Scanner(System.in);
        int n = in.nextInt();
        for (int i = 0; i < n; i++) {
            int k = in.nextInt();
            System.out.println(k + 1);
        }
    }

}
